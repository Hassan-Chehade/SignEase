// Normalize Arabic text: treat all alef variants and ya variants as equal
function normalize(s) {
  return s
    .replace(/[أإآا]/g, 'ا')
    .replace(/[يى]/g, 'ي')
}

export function suggestWords(prefix, max = 6) {
  if (!prefix || prefix.trim() === '') return []
  const normPrefix = normalize(prefix.trim())
  return ARABIC_WORDS
    .filter(w => normalize(w).startsWith(normPrefix))
    .slice(0, max)
}
